fun ArrayList<EasyModel>.save(){
    this.forEach {
        when(it.value){
            is String -> EasyStore.save(it.key, it.value)
            is Int -> EasyStore.save(it.key, it.value)
            is Boolean -> EasyStore.save(it.key, it.value)
            is Float -> EasyStore.save(it.key, it.value)
            is Long -> EasyStore.save(it.key, it.value)
        }
    }
}

